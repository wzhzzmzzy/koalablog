import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

interface PackageExportConditions {
  browser?: string
  default?: string
}

interface SveltePackageJson {
  version: string
  exports: Record<string, string | PackageExportConditions>
}

const projectRoot = process.cwd()
const projectPackage = JSON.parse(await readFile(path.join(projectRoot, 'package.json'), 'utf8')) as {
  dependencies?: Record<string, string>
}

function exactDependencyVersion(name: string) {
  const version = projectPackage.dependencies?.[name]
  if (!version || !/^\d+\.\d+\.\d+$/.test(version))
    throw new Error(`package.json must pin ${name} to an exact version before preparing the Svelte toolchain`)
  return version
}

const unocssVersion = exactDependencyVersion('@unocss/core')
const unocssPresetVersion = exactDependencyVersion('@unocss/preset-uno')
if (unocssPresetVersion !== unocssVersion)
  throw new Error(`UnoCSS core ${unocssVersion} and preset ${unocssPresetVersion} must use the same exact version`)

const toolchainVersions = {
  svelte: exactDependencyVersion('svelte'),
  rollup: exactDependencyVersion('@rollup/browser'),
  svelteLanguage: exactDependencyVersion('@replit/codemirror-lang-svelte'),
  unocss: unocssVersion,
}
const expectedVersion = toolchainVersions.svelte

const sveltePackagePath = fileURLToPath(import.meta.resolve('svelte/package.json'))
const svelteRoot = path.dirname(sveltePackagePath)
const sveltePackage = JSON.parse(await readFile(sveltePackagePath, 'utf8')) as SveltePackageJson
if (sveltePackage.version !== expectedVersion)
  throw new Error(`Installed Svelte ${sveltePackage.version} does not match package.json ${expectedVersion}`)

async function collectRuntimeModules(directory: string): Promise<string[]> {
  const entries = await readdir(directory, { withFileTypes: true })
  const files = await Promise.all(entries.map(async (entry) => {
    const absolutePath = path.join(directory, entry.name)
    if (entry.isDirectory())
      return collectRuntimeModules(absolutePath)
    return entry.isFile() && entry.name.endsWith('.js') ? [absolutePath] : []
  }))
  return files.flat()
}

function isBrowserRuntimeModule(relativePath: string) {
  return !relativePath.startsWith('src/compiler/')
    && !relativePath.startsWith('src/internal/server/')
    && !relativePath.startsWith('src/server/')
    && !relativePath.endsWith('-server.js')
    && relativePath !== 'src/index-server.js'
}

function runtimeExportPath(value: string | PackageExportConditions) {
  const candidate = typeof value === 'string' ? value : value.browser ?? value.default
  if (!candidate)
    return null
  const relativePath = candidate.replace(/^\.\//, '')
  return isBrowserRuntimeModule(relativePath) ? relativePath : null
}

const entrypoints = Object.fromEntries(Object.entries(sveltePackage.exports).flatMap(([exportName, value]) => {
  const relativePath = runtimeExportPath(value)
  if (!relativePath || relativePath.endsWith('.json') || !relativePath.endsWith('.js'))
    return []
  const specifier = exportName === '.' ? 'svelte' : `svelte/${exportName.replace(/^\.\//, '')}`
  return [[specifier, `svelte/${relativePath}`]]
}))

const runtimeFiles = (await collectRuntimeModules(path.join(svelteRoot, 'src')))
  .map(absolutePath => path.relative(svelteRoot, absolutePath).replaceAll(path.sep, '/'))
  .filter(isBrowserRuntimeModule)
  .sort()
const modules = Object.fromEntries(await Promise.all(runtimeFiles.map(async relativePath => [
  `svelte/${relativePath}`,
  await readFile(path.join(svelteRoot, relativePath), 'utf8'),
])))

const generatedRegistry = `// Generated by scripts/build/prepare-svelte-runtime-registry.ts. Do not edit.\nexport const SVELTE_RUNTIME_REGISTRY = ${JSON.stringify({
  version: sveltePackage.version,
  entrypoints,
  modules,
}, null, 2)} as const\n`
const generatedVersions = `// Generated by scripts/build/prepare-svelte-runtime-registry.ts. Do not edit.
export const SVELTE_TOOLCHAIN_VERSIONS = {
  svelte: '${toolchainVersions.svelte}',
  rollup: '${toolchainVersions.rollup}',
  svelteLanguage: '${toolchainVersions.svelteLanguage}',
  unocss: '${toolchainVersions.unocss}',
} as const
`
const generatedOutputs = [
  {
    path: path.join(projectRoot, 'src/lib/svelte/runtime-registry.generated.ts'),
    content: generatedRegistry,
    staleMessage: 'Svelte runtime registry is stale',
  },
  {
    path: path.join(projectRoot, 'src/lib/svelte/toolchain-versions.ts'),
    content: generatedVersions,
    staleMessage: 'Svelte toolchain versions are stale',
  },
]

if (process.argv.includes('--check')) {
  for (const output of generatedOutputs) {
    const current = await readFile(output.path, 'utf8').catch(() => '')
    if (current !== output.content)
      throw new Error(`${output.staleMessage}; run pnpm run svelte:toolchain:prepare`)
  }
}
else {
  for (const output of generatedOutputs) {
    await mkdir(path.dirname(output.path), { recursive: true })
    const current = await readFile(output.path, 'utf8').catch(() => '')
    if (current !== output.content)
      await writeFile(output.path, output.content, 'utf8')
  }
}
